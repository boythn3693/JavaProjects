    private void btnFilterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFilterActionPerformed
        // TODO add your handling code here:
        Boolean isName = false,
                isDescription = false,
                isAddress = false,
                isPhone = false,
                isNDD = false;
        if (cbTen.isSelected()) {
            isName = true;
        }
        if (cbMota.isSelected()) {
            isDescription = true;
        }
        if (cbDiaChi.isSelected()) {
            isAddress = true;
        }
        if (cbPhone.isSelected()) {
            isPhone = true;
        }
        if (cbNDD.isSelected()) {
            isNDD = true;
        }
        
        String filter = txtSearchField.getText();
        if (filter.equals("") == true) {
            StringHelpers.Message("Vui lòng nhập từ khóa", "Thông báo", 2);
            return;
        }
        
        count = partnerService.countTableFilter(filter, isName, isDescription, isAddress, isPhone, isNDD);
        totalPage = (int)(Math.ceil((float)count/pageSize));
        getHeaderTable();
        
        int c = 0;
        try {
            List<Partner> lists = partnerService.getDataPartnerFilter(filter, isName, isDescription, isAddress, isPhone, isNDD);
            start = (page-1)*pageSize;
            end = pageSize*page;
            if(end > lists.size()){
                end = lists.size();
            }
            List<Partner> listProduct = partnerService.getListByPagination(lists, start, end);
            Iterator iter = listProduct.iterator();
            while (iter.hasNext()) {
                Partner partner = (Partner) iter.next();
                Object[] item = new Object[7];
                    item[0] = partner.getPartnerId();
                    item[1] = ++c + start;
                    item[2] = partner.getPartnerName();
                    item[3] = partner.getDescription();
                    item[4] = partner.getAddress();
                    item[5] = partner.getNumPhone();
                    item[6] = partner.getRepresentFullname();
                    tableModel.addRow(item);
            }
            jTablePartner.getColumnModel().getColumn(0).setMinWidth(0);
            jTablePartner.getColumnModel().getColumn(0).setMaxWidth(0);
        } catch (Exception ex) {
            System.out.println("123");
            System.out.println(ex.toString());
        }
        lblpaging.setText(page + "/" + totalPage);
        lblPage.setText("" + page);
    }//GEN-LAST:event_btnFilterActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String filename, dir;
        JFileChooser c = new JFileChooser();
        int rVal = c.showOpenDialog(frmPartner.this);
        if (rVal == JFileChooser.APPROVE_OPTION) {
            filename = c.getSelectedFile().getName();
            dir = c.getCurrentDirectory().toString();
            try {
                FileInputStream excelFile = new FileInputStream(new File(dir + "/" + filename));
                Workbook workbook = new XSSFWorkbook(excelFile);
                Sheet datatypeSheet = workbook.getSheetAt(0);
                DataFormatter fmt = new DataFormatter();
                Iterator<Row> iterator = datatypeSheet.iterator();
                Row firstRow = iterator.next(); // title row, no insert

                int i=0;
                while (iterator.hasNext()) {
                    Row currentRow = iterator.next();
                    Partner partner = new Partner();
                    partner.setPartnerName(fmt.formatCellValue(currentRow.getCell(0)));
                    partner.setDescription(fmt.formatCellValue(currentRow.getCell(1)));
                    partner.setAddress(fmt.formatCellValue(currentRow.getCell(2)));
                    partner.setNumPhone(fmt.formatCellValue(currentRow.getCell(3)));
                    partner.setRepresentFullname(fmt.formatCellValue(currentRow.getCell(4)));

                    partnerService.insert(partner);
                    i++;
                }
                workbook.close();
                StringHelpers.Message("IMPORT DỮ LIỆU THÀNH CÔNG!\n\n File import: " + filename + " .\n Số dòng import thành công: " + i + " dòng", "Thành công", 1);
                onLoad();

                getHeaderTable();
                getDataPartner();
            } catch (FileNotFoundException e) {
                e.printStackTrace();
                StringHelpers.Message("Import dữ liệu không thành công", "Không thành công", 2);
            } catch (IOException e) {
                e.printStackTrace();
                StringHelpers.Message("Import dữ liệu không thành công", "Không thành công", 2);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed


